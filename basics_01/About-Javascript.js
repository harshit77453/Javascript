// JavaScript has three kinds of variable declarations.

// var
// Declares a variable, optionally initializing it to a value.
//  For example, var x = 42. This syntax can be used to declare both local
//  and global variables, depending on the execution context.

// let
// Declares a block-scoped, local variable, optionally initializing it to a value.
// For example, let y = 13. This syntax can be used to declare a block-scope local variable. 

// const
// Declares a block-scoped, read-only named constant.

// Declaration and Initializer -
// In a statement like let x = 42, the let x part is called a declaration, and the = 42 part is called an initializer. 

// The declaration allows the variable to be accessed later in code without throwing a ReferenceError, while the initializer assigns
// a value to the variable. In var and let declarations, the initializer is optional. If a variable is declared without an 
// initializer, it is assigned the value undefined.

// Global scope: The default scope for all code running in script mode.
// Module scope: The scope for code running in module mode.
// Function scope: The scope created with a function.

//In addition, variables declared with let or const can belong to an additional scope:

// Block scope: The scope created with a pair of curly braces (a block).

// When you declare a variable outside of any function, it is called a global variable, because it is available to any other code
// in the current document. When you declare a variable within a function, it is called a local variable, because it is available 
// only within that function.